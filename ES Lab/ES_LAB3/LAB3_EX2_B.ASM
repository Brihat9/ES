;ES LAB3 EXERCISE B TIMER 1 MODE 1
		ORG 00H

		MOV TMOD,#10H 	; TIMER 1 MODE 1 ;2
		
REPEAT:	MOV R2,#02H		;2

HERE1:	MOV TL1,#10H	;2	
		MOV TH1,#4CH	;2
		ACALL DELAY		;2
		CPL P0.0		;1
		DJNZ R2,HERE1	;2

		MOV R2,#0AH		;2
		
HERE2:	MOV TL1,#14H	;2	
		MOV TH1,#0DCH	;2
		ACALL DELAY		;2
		CPL P0.0		;1
		DJNZ R2,HERE2	;2
		
		SJMP REPEAT		;2
		
DELAY:	SETB TR1		;1
AGAIN:	JNB TF1, AGAIN	; X = 46067 FOR 100ms AND 9200 FOR 20ms
		CLR TR1			;1
		CLR TF1			;1
		RET				;2
		
		END