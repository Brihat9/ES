		JC DONE
		INC R4
		AJMP AGAIN
DONE:	ADD A,#12H
		MOV R5,A
		
LOOP:	MOV P0,R4
		ACALL DELAY
		MOV P0,R5
		ACALL DELAY
		AJMP LOOP

